package com.leetcode.www;

public class LinkedListCycle { // LC 141
    public boolean hasCycle(ListNode head) { // beats 10.93%
        if (head == null || head.next == null) {
            return false;
        }
        ListNode slow = head;
        ListNode fast = head;
        while (fast != null && fast.next != null) {
            slow = slow.next;
            fast = fast.next.next;
            if (slow == fast) {
                return true;
            }
        }
        return false;
    }
}
